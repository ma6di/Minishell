#include "../../includes/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*head;

	head = malloc(sizeof(t_list));
	if (!head)
		return ((void *) NULL);
	head->content = content;
	head->next = NULL;
	return (head);
}
